def get_price(hour):
    """
    根据小时返回对应时段的购电电价（元/kWh）。

    时段划分：
      峰时段 (Peak):   10-14 时, 18-20 时
      平时段 (Flat):   7-9 时, 15-17 时, 21-22 时
      谷时段 (Valley): 0-6 时, 23 时

    参数:
        hour: int, 0-23 的小时索引

    返回:
        float: 对应时段的电价 (元/kWh)
    """
    if (10 <= hour <= 14) or (18 <= hour <= 20):
        return PRICE_PEAK
    elif (7 <= hour <= 9) or (15 <= hour <= 17) or (21 <= hour <= 22):
        return PRICE_FLAT
    else:
        return PRICE_VALLEY


def get_price_array():
    """
    生成一天24小时的电价数组。

    返回:
        list[float]: 长度为24的电价列表，索引即小时 (0-23)
    """
    return [get_price(h) for h in range(24)]


def get_period_type(hour):
    """
    根据小时返回时段类型字符串。

    参数:
        hour: int, 0-23 的小时索引

    返回:
        str: 'peak' / 'flat' / 'valley'
    """
    if (10 <= hour <= 14) or (18 <= hour <= 20):
        return 'peak'
    elif (7 <= hour <= 9) or (15 <= hour <= 17) or (21 <= hour <= 22):
        return 'flat'
    else:
        return 'valley'
